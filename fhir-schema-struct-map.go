func GetFhirResourceInstance(resource_name string) (interface{}, error) {
	FhirStructMap := GetFhirResourceMap()
	resource, ok := FhirStructMap[resource_name]
	if ok == true {
		return resource, nil
	}
	return resource, errors.New("Resource not found")
}